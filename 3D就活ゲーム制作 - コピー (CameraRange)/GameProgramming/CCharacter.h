#ifndef CCHARACTER_H
#define CCHARACTER_H
//ベクトルクラスのインクルード
#include "CVector.h"
//モデルクラスのインクルード
#include "CModel.h"
//マトリクスクラスのインクルード
#include "CMatrix.h"
//タスククラスのインクルード
#include "CTask.h"
//#include "CCollider.h"
//コライダクラスの宣言
class CCollider;
/*
キャラクタークラス
ゲームキャラクタの基本的な機能を定義する
*/
class CCharacter : public CTask {
public:
	//種類
	enum ETag {
		ENONE,	//初期値
		EPOINT,	//ポイント
		EPLAYER,//プレイヤー
		EENEMY, //敵
		EWATER, //水場
		ECHECKPOINT,//中間地点1
		ECHECKPOINT2,//中間地点2
		ECHECKPOINT3,//中間地点3
		EGOALPOINT,//ゴール地点
		EDASHBOARD,//加速床
		EGRASS, //芝生
		EWALL, //壁
		EJUMPER, //ジャンプ台
	};
	ETag mTag;
	CVector mPosition; //位置
	CVector mRotation; //回転
	CVector mScale; //拡大縮小
	CModel *mpModel; //モデルのポインタ
	CMatrix mMatrix; //合成行列
	CMatrix mMatrixTranslate; //平行移動行列
	CMatrix mMatrixRotate; //回転行列
	CMatrix mMatrixScale; //拡大縮小行列

	CCharacter();
	~CCharacter();
	//更新処理
	void Update();
	//描画処理
	void Render();
	//衝突処理
	virtual void Collision(CCollider *mycol, CCollider *youcol) {}
	//描画処理の有無
	bool isRender;
};

#endif
